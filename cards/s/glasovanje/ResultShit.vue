<template>
  <div class="result">
    <donut-chart
      :class="['donut-chart', `option-${option}`]"
      :section-data="chartData"
    />
    <div>
      <div class="text">{{ translatedOption }}</div>
      <div v-if="option !== 'cant_compute'" class="percentage">{{ Math.round(score) }} %</div>
    </div>
  </div>
</template>

<script>
import DonutChart from 'components/DonutChart.vue';

export default {
  name: 'GlasovanjeSejeResultShit',
  components: { DonutChart },
  props: {
    chartData: {
      type: Array,
      default: () => [],
    },
    option: {
      type: String,
      default: '',
    },
    score: {
      type: Number,
      default: 0,
    },
  },
  computed: {
    translatedOption() {
      switch (this.option) {
        case 'for':
          return this.$t('vote-for');
        case 'against':
          return this.$t('vote-against');
        case 'abstain':
          return this.$t('vote-abstain-plural');
        case 'absent':
          return this.$t('vote-absent-plural');
        case 'cant_compute':
          return this.$t('vote-no-majority');
        default:
          return '';
      }
    },
  },
};
</script>

