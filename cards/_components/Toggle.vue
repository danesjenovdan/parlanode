<template>
  <div class="toggle">
    <div
      v-for="(optionLabel, optionValue) in options"
      :key="optionValue"
      :class="['option', { 'is-selected' : optionValue === value }]"
      @click="handleClick(optionValue)"
    >
      {{ optionLabel }}
    </div>
  </div>
</template>

<script>
export default {
  name: 'Toggle',
  props: {
    options: {
      type: Object,
      default: () => ({}),
    },
    value: {
      type: [String, Number, Boolean],
      default: '',
    },
  },
  methods: {
    handleClick(newValue) {
      this.$emit('input', newValue);
    },
  },
};
</script>

