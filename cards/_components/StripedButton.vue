<template>
  <div :class="className">
    <div v-if="smallText" :class="['small-text', {'is-uppercase': isUppercase}]">
      {{ smallText }}
    </div>
    <div v-if="text" class="text">{{ text }}</div>
  </div>
</template>

<script>
export default {
  name: 'StripedButton',
  props: {
    selected: {
      type: Boolean,
      default: false,
    },
    smallText: {
      type: String,
      default: '',
    },
    isUppercase: {
      type: Boolean,
      default: true,
    },
    text: {
      type: String,
      default: '',
    },
    color: {
      type: String,
      default: '',
    },
    disabled: {
      type: Boolean,
      default: false,
    },
    stripePosition: {
      type: String,
      default: 'top',
      validator: value => ['top', 'bottom'].indexOf(value) > -1,
    },
  },
  computed: {
    className() {
      return [
        'striped-button',
        { 'is-disabled': this.disabled },
        { 'is-selected': this.selected },
        this.color,
        `has-stripe-on-${this.stripePosition}`,
      ];
    },
  },
};
</script>

